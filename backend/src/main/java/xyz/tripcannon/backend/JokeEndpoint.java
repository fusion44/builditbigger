package xyz.tripcannon.backend;

import com.google.api.server.spi.config.Api;
import com.google.api.server.spi.config.ApiMethod;
import com.google.api.server.spi.config.ApiNamespace;

import xyz.tripcannon.jokes.JokesRepository;

/**
 * An endpoint class we are exposing
 */
@Api(
        name = "myApi",
        version = "v1",
        namespace = @ApiNamespace(
                ownerDomain = "backend.tripcannon.xyz",
                ownerName = "backend.tripcannon.xyz",
                packagePath = ""
        )
)
public class JokeEndpoint {

    /**
     * A simple endpoint method that takes a name and says Hi back
     */
    @ApiMethod(name = "getJoke")
    public Joke getJoke() {
        String jokeText = JokesRepository.getJoke();
        Joke response = new Joke();
        response.setData(jokeText);

        return response;
    }

}
